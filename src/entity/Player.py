
def __init__(self):
    self.score = 0
    self.hand = []
    self.crib = []

def setHand(self, hand):
    self.hand = hand

def getHand(self):
    return self.hand

def setCrib(self, crib):
    self.crib = crib

def getCrib(self):
    return self.crib

def setScore(self, score):
    self.score = score

def getScore(self):
    return self.score